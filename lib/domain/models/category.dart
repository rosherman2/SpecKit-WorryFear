/// [Enum] Category types for scenarios in the Worry vs Fear game.
/// Purpose: Distinguishes between immediate danger (fear) and future concerns (worry).
///
/// Fear represents immediate, present danger requiring quick response.
/// Worry represents future-focused "what if" thinking about potential problems.
enum Category {
  /// Immediate danger or threat in the present moment.
  /// Example: "A car just swerved toward me"
  fear,

  /// Future-focused concern or "what if" thinking.
  /// Example: "What if I never find a partner?"
  worry,
}
